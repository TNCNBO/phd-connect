def get_school_level(school_name: str) -> str:
    """根据学校名返回 '985' / '211' / '双非'"""
    # Exact match
    if school_name in SCHOOLS_985:
        return "985"
    if school_name in SCHOOLS_211:
        return "211"

    # Try with 大学/学院 suffix variations
    base = school_name.rstrip("大学学院")
    for s in SCHOOLS_985:
        if base in s or s in school_name:
            return "985"
    for s in SCHOOLS_211:
        if base in s or s in school_name:
            return "211"

    # Try aliases
    if school_name in SCHOOL_ALIASES:
        return get_school_level(SCHOOL_ALIASES[school_name])

    return "双非"


